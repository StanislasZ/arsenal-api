    public void generate2() {
        //构建代码生成器对象
        AutoGenerator mpg = new AutoGenerator();

        //1. 全局配置
        GlobalConfig gc = new GlobalConfig();
        String projectPath = System.getProperty("user.dir");
        gc.setOutputDir(projectPath + "/src/main/java");
        gc.setOpen(false);   //是否打开输出目录
        gc.setFileOverride(false);  //是否覆盖已有文件
        gc.setServiceName("%sService");
        gc.setIdType(IdType.AUTO);
        //TIME_PACK 使用 java.time 包下的 java8 新的时间类型
        gc.setDateType(DateType.TIME_PACK);
        gc.setSwagger2(true);  //开启swagger2模式

        mpg.setGlobalConfig(gc);

        //2. 数据源配置
        DataSourceConfig dsc = new DataSourceConfig();
        dsc.setUrl("jdbc:mysql://localhost:3306/arsenal?useUnicode=true&useSSL=false&characterEncoding=utf8");
        dsc.setDriverName("com.mysql.cj.jdbc.Driver");
        dsc.setUsername("root");
        dsc.setPassword("sixinbuxi");
        dsc.setDbType(DbType.MYSQL);

        mpg.setDataSource(dsc);

        //3. 包的配置
        PackageConfig pc = new PackageConfig();
        pc.setModuleName("user");
        pc.setParent("com.zrylovestan.arsenal.modules");

        mpg.setPackageInfo(pc);

        //4. 策略配置
        StrategyConfig sc = new StrategyConfig();
        sc.setInclude("user");
        sc.setNaming(NamingStrategy.underline_to_camel);
        sc.setColumnNaming(NamingStrategy.underline_to_camel);
        sc.setEntityLombokModel(true);  //开始lombok支持
        sc.setLogicDeleteFieldName("deleted");
        sc.setVersionFieldName("version");
        sc.setRestControllerStyle(true);  //开始controller的restful命名
//        sc.setControllerMappingHyphenStyle(false);

        TableFill createTime = new TableFill("create_time", FieldFill.INSERT);
        TableFill updateTime = new TableFill("update_time", FieldFill.INSERT_UPDATE);

        List<TableFill> tableFills = new ArrayList<>();
        tableFills.add(createTime);
        tableFills.add(updateTime);
        sc.setTableFillList(tableFills);

        mpg.setStrategy(sc);

        mpg.execute();

    }



